                    <h2>Základní informace</h2>
<h3>Termín a místo</h3>
<p>Víkend 12.&nbsp;-&nbsp;13.&nbsp;11. 2016 v Moravském krasu (místo startu bude upřesněno).</p>
<h3>Účastníci</h3>
<p>Akce je učena pro tříčlenné nebo čtyřčlenné týmy (15 a více let). Na výkonnosti ani pohlaví nezáleží; na své si přijdou namakaní borci, rekreační sportovci i občasní dobrodruhové.</p>
<h3>Cena</h3>
<p>Cena akce je 850,- Kč/tým. Zahrnuje tisk map, zajištění materiálu, pronájem základny a občerstvení v cíli.</p>
<h3>Storno podmínky</h3>
<p>Storno poplatky činí 30&nbsp;% (odhlášení více než 30 dní před započetím akce), 50&nbsp;% (30 až 10 dní), 90&nbsp;% (9 až 1 den předem) či 100&nbsp;% (v&nbsp;den započetí akce či později). 
V případě zrušení akce ze strany organizátorů vracíme 100&nbsp;% uhrazené částky. Je možné měnit složení týmu (= najít za sebe náhradu).</p>
<h3>Přihlašování</h3>
<p>Přihlašování bude spuštěno 10. října 2016 v 20:20. Kapacita závodu je stanovena na 50 týmů. Proto neváhejte a přihašte se co nejdříve. Týmy, kteří se účastnili KRAZu na jaře dostanou přednostní kód pro přihlašování, první tři týmy z jara mají startovné zdarma.</p>

<h3>Dotazy</h3>
<p>Případné dotazy směřujte buď na <a class="link" href="https://www.facebook.com/events/179944522443155/" target="_blank" title="KRAZ na Facebooku">Facebook</a> (pozvěte i svoje kamarády) nebo na&nbsp;e-mailovou adresu
<a href="mailto:zavodkraz@gmail.com">zavodkraz@gmail.com</a>.

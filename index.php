                    <h2>Pravidla</h2>
                    <p><strong>POZOR</strong>: Níže uvedná pravidla jsou pouze informační a mohou se měnit. 
                    Konečnou verzi pošleme všem přihlášným e-mailem týden před hrou.
                    </p>

<h3>Start a průběh závodu - podzim 2016:</h3> 

<ul class="tleft">
<li>Cílem závodu je projít všemi kontrolními stanovišti v&nbsp;daném pořadí 
(není-li určeno jinak) až do cíle, jehož umístění není předem známo. Volba trasy mezi stanovišti je na vašem uvážení.</li>
<li>Maximální počet týmů je 50 + 5 později vybraných na divokou kartu.</li>
<li>Po cestě není nutné plnit žádné další úkoly na stanovištích ani žádné šifry. Závod je zajímavý zejména svými úseky a atmosférou denního a nočního lesa.</li>
<li>Závodí výhradně týmy o <strong>3&ndash;4 lidech</strong>, všichni členové týmu musí být <strong>starší patnácti let</strong>. Pokud chtějí soutěžit mladší účastníci, kontaktujte nás soukromě.</li> 
<li>Registrace týmů bude spuštěna 10.10. ve 20:20. Start závodu je<strong> v sobotu 12. listopadu 2016 odpoledne</strong>. </li> 
<li><strong>Místo startu</strong> bude zasláno výhradně registrovaným týmům 3 dny před začátkem závodu. Místo se nachází v bezprostřední blízkosti Brna v dosahu IDS&nbsp;JMK</li>
<li>Polohu stanoviště se dozvíte vždy na předchozím bodě. Od okamžiku startu máte <strong>20&nbsp;hodin</strong></li>
</ul>
<h3>V cíli</h3>
<p>Cíl závodu bude na místě s s&nbsp;možností občerstvení (v&nbsp;ceně), přespáním, sprchou atd.</p>
<p>Vyhlášení výsledků proběhne, jakmile bude jasné pořadí na prvních třech místech.</p> 

<h3>Povinná výbava</h3>

 
<ul>
<li>Buzola nebo kompas</li>
<li>Mobilní telefon</li>
<li>Digitální fotoaparát</li>
<li>GPS navigace</li>

<p>Tyto předměty musí být oddělené, není tedy možné mít jediný telefon, na kterém bude i&nbsp;buzola a kompas, celkem tedy musíte mít přinejmenším čtyři různé předměty.</p>
</ul>

<h3>Doporučená výbava</h3>
<ul>
<li>Čelovka pro každého člena týmu</li>
<li>Silná baterka</li>
<li>Náhradní baterie</li>
<li>Náhradní baterie</li>
<li>Náhradní baterie</li>
</ul>

<p>Další doporučené vybavení: náhradní boty a oblečení s ohledem na aktuální počasí a teplotu. Dostatek kalorií a tekutin na dvacet hodin. Závod je dlouhý a vede náročným terénem. Spacák a karimatka nejsou během závodu nutné (v rozumné míře zajistíme převoz věcí ze startu do cíle).</p>


<h3>Zakázané vybavení:</h3>
<p><strong>Je přísně zakázáno používat při závodě vlastní mapy.</strong> Smíte využít jen těch map, které dostanete od nás. Máte-li mapu v&nbsp;mobilu či GPSce, musí být možné ji vypnout, jinak nebude možné takovou navigaci pro závod uznat.</p> 

<p>Během závodu je zakázano k přesunům používat motorové prostředky, kola, koloběžky, koně, osly, slony a jiné prostředky mimo vlastní nohy.</p> 

<h3>Prostor závodu:</h3>
<p>Kopcovitý, lesnatý terén různého charakteru. Během závodu se budete pohybovat prakticky jen v lese.</p> 


                <h2>Outdoorový závod KRAZ</h2>
                <p class="italic">Hluboké lesy Moravského krasu čekají. Co ještě zvládneš a&nbsp;co už je nad tvoje síly? A&nbsp;máš se o&nbsp;koho opřít?</p>
                <p>KRAZ je outdoorový závod s&nbsp;několikaletou tradicí, pořádaný sdružením <a href="http://www.halahoj.org/">Halahoj</a> a nově i pod&nbsp;hlavičkou <a href="http://www.instruktori.cz/">
                   Instruktorů Brno</a>. Čtyřčlenné týmy překonávají trasu náročným terénem a hledají stanoviště, která jim prozradí další úsek cesty.
                   O&nbsp;celkovém výsledku rozhoduje čas dokončení závodu. 
                   Závod začíná v sobotu odpoledne a končí v neděli, počítejte, že se přes noc moc nevyspíte.
                </p>
                <p>Závod se tradičně koná v&nbsp;oblasti Moravského krasu severně od&nbsp;Brna. Trať není předem známá, týmy se&nbsp;samy orientují podle mapy nebo jiných vodítek, která najdou na stanovišti.
                Řádově se&nbsp;jedná o&nbsp;desítky (cca&nbsp;20-40) kilometrů. Cestou vás může potkat prakticky cokoli, od neúplných map přes outdoorové aktivity až po rébusy a hádanky.
                </p>
                <p> Pro lepší představu se&nbsp;podívejte na&nbsp;web <a href="http://zavodkraz.cz/stary" target="_blank">minulých ročníků</a>.</p>
                <h3>Na co se můžete těšit</h3>
                <p>Krásnou příroda, nečekané úkoly, krev, dřinu, pot a slzy.</p>
                <h3>Čeho se nemusíte bát</h3>
                <p>Deště, zimy, smrti z vyčerpání a posměšků od soupeřů.</p>
